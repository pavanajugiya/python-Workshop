# Even or Odd
even_odd = lambda x: "Even" if x % 2 == 0 else "Odd"
print(even_odd(10))

#Square
square = lambda x: x * x
print(square(8))

# Celsius to Fahrenheit
fahrenheit = lambda c: (c * 9/5) + 32
print(fahrenheit(30))

# Maximum of Two Numbers
maximum = lambda x, y: x if x > y else y
print(maximum(45, 25))

